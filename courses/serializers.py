    user_has_paid = serializers.SerializerMethodField()
    
    def get_photo(self, obj):
        """Return full URL for photo"""
        if obj.photo:
            request = self.context.get('request')
            if request:
                return request.build_absolute_uri(obj.photo.url)
            return obj.photo.url
        return None
    
